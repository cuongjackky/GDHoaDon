    private void ThangCBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ThangCBoxActionPerformed
        String Nam = NamCBox.getSelectedItem().toString();
        int nam = Integer.parseInt(Nam);
        int thang = Integer.parseInt(ThangCBox.getSelectedItem().toString());
        int ngay=0;
        Boolean Nhuan = false;
        if(nam%400==0||(nam%100!=0||nam%4==0)){
            Nhuan = true;
        }
        switch (thang){
            case 1: case 3: case 5: case 7: case 8: case 10: case 12:{
                ngay =31;
                break;
                
            }
            case 4: case 6: case 9: case 11:{
                ngay =30;
                break;
            }
            case 2:{
                if(Nhuan){
                    ngay =29;
                }
                else ngay =28;
            }
             
        }
        Vector<String> Ngay = new Vector();
        for(int i=1;i<=ngay;i++){
                    Ngay.add(Integer.toString(i));
                }
        DefaultComboBoxModel dcbm = new DefaultComboBoxModel(Ngay);
        NgayCBox.setModel(dcbm);
    }//GEN-LAST:event_ThangCBoxActionPerformed

