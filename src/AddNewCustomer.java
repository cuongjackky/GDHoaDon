    public Boolean addNewCustomer(){
        
        String MaKH = maKHTxt.getText();
        String ho = HoTxt.getText();
        if(ho.equals("")){
            
            JOptionPane.showMessageDialog(null, "Nhập họ");
            return false;
        }
        String ten=TenTxt.getText();
        if(ten.equals("")){
            
            JOptionPane.showMessageDialog(null, "Nhập tên");
            return false;
        }
        String ngay =NgayCBox.getSelectedItem().toString();
        if(ngay.length()==1){
            ngay='0'+ngay;
        }
        String thang =ThangCBox.getSelectedItem().toString();
        if(thang.length()==1){
            thang='0'+thang;
        }
        String nam = NamCBox.getSelectedItem().toString();
        String Dob = nam+thang+ngay;
        
        String soNha = soNhaTxt.getText();
        if(soNha.equals("")){
            
            JOptionPane.showMessageDialog(null, "Nhập số nhà");
            return false;
        }
        String duong = duongTxt.getText();
        if(duong.equals("")){
            
            JOptionPane.showMessageDialog(null, "Nhập đường");
            return false;
        }
        String phuong = phuongTxt.getText();
        if(phuong.equals("")){
            
            JOptionPane.showMessageDialog(null, "Nhập phường");
            return false;
        }
        String quan =quanTxt.getText();
        if(quan.equals("")){
            
            JOptionPane.showMessageDialog(null, "Nhập quận");
            return false;
        }
        String tp = tpTxt.getText();
        if(tp.equals("")){
            
            JOptionPane.showMessageDialog(null, "Nhập thành phố");
            return false;
        }
        String sdt = sdtTxt.getText();
        if(sdt.equals("")){
            
            JOptionPane.showMessageDialog(null, "Nhập số điện thoại");
            return false;
        }
        if(new DBUpdater().AddNewCustomer(MaKH, ho, ten, Dob, soNha, duong, phuong, quan, tp, sdt)){
            JOptionPane.showMessageDialog(null, "Thêm thành công");
            return true;
        }
        else{
            JOptionPane.showMessageDialog(null, "Lỗi");
            return true;
        }
    }

