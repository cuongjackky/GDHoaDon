    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        String maHD = maHDTxt.getText();
        String maKH = (String)maKHCBox.getSelectedItem();
        String ngayLap = ngayLapTxt.getText();
        String maSP = (String)MaSpCBox.getSelectedItem();
        
        String soLuong = soLuongTxt.getText();
        String giaBan = new DBUpdater().getPriceofID((String)MaSpCBox.getSelectedItem());
        String giaGiam = giaGiamTxt.getText();
        dm.addRow(new String[]{maHD,maSP,soLuong,giaBan,giaGiam});
        jTable1.setModel(dm);
    }//GEN-LAST:event_AddBtnActionPerformed

    private void OKBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OKBtnActionPerformed
        int nRow = dm.getRowCount();
        int nCol = dm.getColumnCount();
        Object[][] tableData = new Object[nRow][nCol];
        for (int i = 0 ; i < nRow ; i++)
            for (int j = 0 ; j < nCol ; j++)
            tableData[i][j] = dm.getValueAt(i,j);
        
        String maSP = (String)MaSpCBox.getSelectedItem();
        String soLuong = soLuongTxt.getText();
        String maHD = maHDTxt.getText();
        String maKH = (String)maKHCBox.getSelectedItem();
        String ngayLap = ngayLapTxt.getText();
        if(new DBUpdater().add(maHD, maKH, ngayLap )&&
        new DBUpdater().add_2(tableData,nRow,nCol)&&new DBUpdater().updateSupplies(maSP, soLuong)){
            JOptionPane.showMessageDialog(null, "Thêm thành công!");
             this.setVisible(false);
             
        }
        else {
            JOptionPane.showMessageDialog(null, "Thêm thất bại!");
        }
       
        
    }//GEN-LAST:event_OKBtnActionPerformed

