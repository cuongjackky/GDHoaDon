    private void soLuongTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_soLuongTxtActionPerformed
        String soLuong = soLuongTxt.getText();
        String tonKho = new DBUpdater().getSuppliesCount((String)MaSpCBox.getSelectedItem());
        if(Integer.parseInt(soLuong)>Integer.parseInt(tonKho)){
            JOptionPane.showMessageDialog(null, "Số lượng sản phẩm lớn hơn tồn kho!");
            soLuongTxt.setText(tonKho);
        }
    }//GEN-LAST:event_soLuongTxtActionPerformed

